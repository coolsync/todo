# 每跑一圈， 做三个俯卧撑, use while nested
i = 0

while i < 5:
    print('跑了一圈 i= %d' % i)
    j = 0
    while j < 3:
        print('做个俯卧撑 j = %d' % j)
        j += 1
    i += 1
print('end')
